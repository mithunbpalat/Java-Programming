public class multiply{
    public static void main(String[] args){
        int i=1,ans;
        while(i<11)
        {
            ans=5*i;
            System.out.println("5 * "+i+" = "+ans);
            i++;
        }
        for(int j=1;j<21;j++)
        {
            ans=10*j;
            System.out.println("10 * "+j+" = "+ans);
        }
    }
}